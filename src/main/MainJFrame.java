    private void ejecutarAyuda() {
        try {
            // Cargar el fichero de ayuda
            File file = new File("help" + File.separator + "help_set.hs");
            URL hsURL = file.toURI().toURL();
            
            // Crea el HelpSet y el HelpBroker
            HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
            HelpBroker hb = helpset.createHelpBroker();
            
            // Pone ayuda al item del men√∫ al pulsarlo y a F1 en ventana principal y secundaria
            hb.enableHelpOnButton(this.jMenuItemAyuda, "ventana_principal", helpset);
            hb.enableHelpKey(getRootPane(), "ventana_principal", helpset);
            hb.enableHelpKey(jButtonPrincipal, "ventana_principal", helpset);
            hb.enableHelpKey(this.jButtonSecundaria, "ventana_secundaria", helpset);
            hb.enableHelpOnButton(this.jButtonPrincipal, "ventana_principal", helpset);
            hb.enableHelpOnButton(this.jButtonSecundaria, "ventana_secundaria", helpset);
        } catch (MalformedURLException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HelpSetException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
